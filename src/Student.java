class Student {
    private String name;
    private int age;

    // Costruttore
    public Student(String name, int age) {
        this.name = name;
        this.age = age;
    }

    // Metodi per ottenere le informazioni
    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}